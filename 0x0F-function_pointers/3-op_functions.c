/**
 * op_add - do addition
 * @a: addend
 * @b: addend
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: subtrahend
 * @b: minuend
 *
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: multiplicand
 * @b: multiplicand
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: d
 * @b: c
 *
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder
 * @a: s
 * @b: dd
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
